
def add(a, b):
    return a + b

def sub(a, b):
    return a - b

def mul(a, b):
    return a * b

def div(a, b):
    if b != 0:
        return a / b
    else:
        return "Cannot divide by zero"

def modulus(a, b):
    return a % b

def floordivision(a, b):
    return a // b

def exponential(a, b):
    return a ** b
